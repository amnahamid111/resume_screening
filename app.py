    input_data = {feature: 0 for feature in feature_names}

    # -------------------------------
    # Numeric Features
    # -------------------------------

    input_data["Experience_Years"] = experience
    input_data["Skill_Match_Percentage"] = skill_match
    input_data["Projects_Count"] = projects
    input_data["Expected_Salary"] = salary

    # -------------------------------
    # English Level
    # -------------------------------

    english_map = {
        "Beginner": 0,
        "Intermediate": 1,
        "Advanced": 2
    }

    input_data["English_Level"] = english_map[english]

    # -------------------------------
    # Job Role
    # -------------------------------

    job_column = "Job_Role_" + job_role

    if job_column in input_data:
        input_data[job_column] = 1

    # -------------------------------
    # Education
    # -------------------------------

    education_column = "Education_" + education

    if education_column in input_data:
        input_data[education_column] = 1

    # -------------------------------
    # Certification
    # -------------------------------

    certification_column = "Certifications_" + certification

    if certification_column in input_data:
        input_data[certification_column] = 1

    # -------------------------------
    # Skills
    # -------------------------------

    for skill in skills:

        if skill in input_data:
            input_data[skill] = 1

    # -------------------------------
    # Create DataFrame
    # -------------------------------

    input_df = pd.DataFrame([input_data])

    # Keep the exact feature order
    input_df = input_df[feature_names]

